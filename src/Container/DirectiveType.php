<?php

declare(strict_types=1);

namespace Vasoft\Joke\Templator\Container;

/**
 * Типы директив шаблонизатора.
 *
 * Определяет поведение парсера при встрече конкретной директивы в шаблоне.
 * Используется для построения корректного абстрактного синтаксического дерева (AST)
 * и управления стеком открытых блоков.
 */
enum DirectiveType: int
{
    /**
     * Неизвестный или нераспознанный тип директивы.
     */
    case UNKNOWN = 0;
    /**
     * Открывающая директива блока (например, {% if %}, {% foreach %}).
     * Требует наличия соответствующей закрывающей директивы и создает новый уровень вложенности в AST.
     */
    case BEGIN = 1;
    /**
     * Закрывающая директива блока (например, {% /if %}, {% /foreach %}).
     * Завершает текущий контекст и извлекает узел из стека парсера.
     */
    case END = 2;
    /**
     * Одиночная директива (например, {% include %}, {% csrf %}).
     * Не имеет закрывающего тега и не создает вложенного контекста для дочерних элементов.
     */
    case SINGLE = 3;
    /**
     * Директива ветвления (например, {% else %}, {% elseif %}, {% case %}).
     * Изменяет контекст записи детей внутри текущего открытого блока, не создавая нового узла в стеке.
     */
    case BRANCH = 4;
}
