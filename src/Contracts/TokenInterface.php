<?php

declare(strict_types=1);

namespace Vasoft\Joke\Templator\Contracts;

/**
 * Базовый интерфейс для всех токенов лексера.
 *
 * Токен представляет собой минимальную смысловую единицу шаблона,
 * полученную в результате лексического анализа.
 */
interface TokenInterface
{
    /**
     * Исходное содержимое токена.
     *
     * Для текстовых токенов — это сам текст.
     * Для директив и выражений — это содержимое между открывающими и закрывающими разделителями
     * (без самих разделителей, например, без '{{' и '}}').
     */
    public string $raw {
        get;
    }
}
