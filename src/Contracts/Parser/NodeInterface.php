<?php

declare(strict_types=1);

namespace Vasoft\Joke\Templator\Contracts\Parser;

/**
 * Базовый интерфейс для всех узлов абстрактного синтаксического дерева (AST).
 *
 * Узел представляет собой элемент структуры шаблона после парсинга.
 * Это может быть текстовый блок, выражение для вывода, директива управления
 * или другой структурный элемент.
 *
 * Реализации этого интерфейса используются парсером для построения дерева
 * и процессорами (компилятором/рендерером) для генерации итогового результата.
 */
interface NodeInterface
{
    /**
     * @var class-string
     */
    public string $tokenClass {
        get;
    }
}
